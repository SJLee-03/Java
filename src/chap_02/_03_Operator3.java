package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // 5가 3보다 크다 true
        System.out.println(5 >= 3); // 5가 3보다 크거나 작다 true
        System.out.println(5 < 3); // 5가 3보다 작다 false
        System.out.println(5 <= 3); // 5가 3보다 작거나 같다 false
        System.out.println(5 == 3); // 5가 3과 같다 false
        System.out.println(5 != 3); // 5가 3과 다르다 true
    }
}
